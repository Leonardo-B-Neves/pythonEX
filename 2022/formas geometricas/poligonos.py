# função anonima retorna area do circulo
Area_circulo = lambda raio: 3.14 * (raio ** 2)
# função anonima retorna area do retangulo
Area_retangulo = lambda largura, comprimento: largura * comprimento
# função anonima retorna area do triangulo equilatero
Area_triangulo = lambda lado: ((lado**2)*(3**(1/2)))/4
